class User:
    def __init__(self):
        self.__id = str()
        self.__firstname = str()
        self.__lastname = str()
        self.__email = str()
        self.__gender = str()
        self.__password = str()

    def getId():
        return self.__id

    def setId(id):
        self.__id = id

    def getFirstname():
        return self.__firstname

    def setFirstname(firstname):
        self.__firstname = firstname

    def getLastname():
        return self.__lastname

    def setLastname(lastname):
        self.__lastname = lastname

    def getEmail():
        return self.__email

    def setEmail(email):
        self.__email = email

    def getGender():
        return self.__gender

    def setGender(gender):
        self.__gender = gender

    def getPassword():
        self.__password

    def setPassword(password):
        self.__password = password